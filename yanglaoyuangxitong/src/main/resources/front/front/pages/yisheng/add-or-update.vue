        }
        },
        components: {
            wPicker
        },
        computed: {
            baseUrl() {
                return this.$base.url;
            },
        },
        async onLoad(options) {

		/*下拉框*/
			let sexTypesParams = {
                page: 1,
                limit: 100,
                dicCode: 'sex_types',
            }
			let sexTypes = await this.$api.page(`dictionary`, sexTypesParams);
			this.sexTypesOptions = sexTypes.data.list
		/*下拉框*/
			let yishengTypesParams = {
                page: 1,
                limit: 100,
                dicCode: 'yisheng_types',
            }
			let yishengTypes = await this.$api.page(`dictionary`, yishengTypesParams);
			this.yishengTypesOptions = yishengTypes.data.list


            // 如果是更新操作
            if (options.id) {
                this.ruleForm.id = options.id;
                // 获取信息
                let res = await this.$api.info(`yisheng`, this.ruleForm.id);
                this.ruleForm = res.data;
            }
            if(options.yishengId){
                this.ruleForm.yishengId = options.yishengId;
            }
            // 跨表
            // this.styleChange()
        },
        methods: {
            // 下拉变化
            sexTypesChange(e) {
                this.sexTypesIndex = e.target.value
                this.ruleForm.sexValue = this.sexTypesOptions[this.sexTypesIndex].indexName
                this.ruleForm.sexTypes = this.sexTypesOptions[this.sexTypesIndex].codeIndex
            },
            yishengTypesChange(e) {
                this.yishengTypesIndex = e.target.value
                this.ruleForm.yishengValue = this.yishengTypesOptions[this.yishengTypesIndex].indexName
                this.ruleForm.yishengTypes = this.yishengTypesOptions[this.yishengTypesIndex].codeIndex
            },


