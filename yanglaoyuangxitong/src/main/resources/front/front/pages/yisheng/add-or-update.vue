            },




            getUUID() {
                return new Date().getTime();
            },
            async onSubmitTap() {
				if ((!this.ruleForm.username)) {
                    this.$utils.msg(`账户不能为空`);
                    return
                }
				if ((!this.ruleForm.yishengName)) {
                    this.$utils.msg(`医生名称不能为空`);
                    return
                }
				if ((!this.ruleForm.yishengPhone) && (!this.$validate.isMobile(this.ruleForm.yishengPhone))) {
                    this.$utils.msg(`医生手机号不能为空并且需要输入正确格式`);
                    return
                }
				if ((!this.ruleForm.yishengPhoto)) {
                    this.$utils.msg(`医生头像不能为空`);
                    return
                }
				if ((!this.ruleForm.sexTypes)) {
                    this.$utils.msg(`性别不能为空`);
                    return
                }
				if ((!this.ruleForm.yishengEmail) && (!this.$validate.isEmail(this.ruleForm.yishengEmail))) {
                    this.$utils.msg(`医生邮箱不能为空并且需要输入正确格式`);
                    return
                }
				if ((!this.ruleForm.yishengNumber) && (!this.$validate.isIntNumer(this.ruleForm.yishengNumber)) && this.ruleForm.yishengNumber >0) {
                    this.$utils.msg(`挂号人数限制不能为空，不能小于0 格式为数字`);
                    return
                }
				if ((!this.ruleForm.yishengNewmoney) && (!this.$validate.isNumber(this.ruleForm.yishengNewmoney)) && this.ruleForm.yishengNewmoney >0) {
                    this.$utils.msg(`挂号价格不能为空，不能小于0 格式为数字或小数`);
                    return
                }
				if ((!this.ruleForm.yishengTime)) {
                    this.$utils.msg(`在诊日期不能为空`);
                    return
                }
				if ((!this.ruleForm.yishengZhicheng)) {
                    this.$utils.msg(`职称不能为空`);
                    return
                }
                if (this.ruleForm.id) {
                    await this.$api.update(`yisheng`, this.ruleForm);
                } else {
                    await this.$api.save(`yisheng`, this.ruleForm);
                }
                uni.setStorageSync('pingluenStateState', true);
                this.$utils.msgBack('提交成功');
            },
            getDate(type) {
                const date = new Date();
                let year = date.getFullYear();
                let month = date.getMonth() + 1;
                let day = date.getDate();
                if (type === 'start') {
                    year = year - 60;
                } else if (type === 'end') {
                    year = year + 2;
                }
                month = month > 9 ? month : '0' + month;;
                day = day > 9 ? day : '0' + day;
                return `${year}-${month}-${day}`;
            },
            toggleTab(str) {
                this.$refs[str].show();
            }
        }
    }
