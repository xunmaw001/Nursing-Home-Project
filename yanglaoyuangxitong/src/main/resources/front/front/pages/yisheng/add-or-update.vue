			yishengPhotoTap() {
                let _this = this;
                this.$api.upload(function(res) {
                    _this.ruleForm.yishengPhoto = 'upload/' + res.file;
                    _this.$forceUpdate();
                    _this.$nextTick(() => {
                        // _this.styleChange()
                    })
                });
            },
			// 日期控件
			yishengTimeConfirm(val) {
                this.ruleForm.yishengTime = val.result;
                this.$forceUpdate();
            },
			// 日期控件
			insertTimeConfirm(val) {
                this.ruleForm.insertTime = val.result;
                this.$forceUpdate();
            },
			// 日期控件
